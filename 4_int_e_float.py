#Tipos int e float
#int -> número inteiro
#o int representa qualquer número positivo ou negativo. int sem sinal é #considerado positivo
print(11) #int
print(-11)#int
print(0)#int

#float -> número com ponto flutuante
#o tipo float representa qualquer número positivo ou negativo
#com ponto flutuante. float sem final é considerado positivo
print(1.1, 10.1) #float
print(0.0, -1,5) #float

#a função type mosntra o tipo que o python inferiu ao valor
print(type("Leonardo"))
print(type(10))
print(type(1.1))

